const keys = Object.keys(en);

function writeLocale(code, data) {
  const missing = keys.filter((k) => !(k in data));
  if (missing.length) console.warn(`${code}: missing ${missing.length} keys`);
  const body = `export default ${JSON.stringify(data, null, 2)};\n`;
  fs.writeFileSync(path.join(__dirname, `${code}.mjs`), body);
  console.log(`wrote ${code}.mjs`, Object.keys(data).length, "keys");
}

